public class FacultyEntrant implements SkeletonEntity {
    private int facultyId;
    private int entrantId;
    private int priority;
    private double sumMarks;

    public int getFacultyId() {
        return facultyId;
    }

    public void setFacultyId(int facultyId) {
        this.facultyId = facultyId;
    }

    public int getEntrantId() {
        return entrantId;
    }

    public void setEntrantId(int entrantId) {
        this.entrantId = entrantId;
    }

    public int getPriority() {
        return priority;
    }

    public void setPriority(int priority) {
        this.priority = priority;
    }

    public double getSumMarks() {
        return sumMarks;
    }

    public void setSumMarks(double sumMarks) {
        this.sumMarks = sumMarks;
    }

    @Override
    public String toString() {
        return "Faculty ID: " + facultyId +
                "Entrant ID: " + entrantId +
                "Priority: " + priority +
                "Summary marks: " + sumMarks;
    }
}
